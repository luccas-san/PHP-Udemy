<?php

    $nome = "Lucas";

    // %s -> String:
    printf("O nome é %s <br>", $nome);

    // %d -> Int:
    $n = 10;

    printf("O número é %d e o outro é %d <br>", $n, 150);


    // %f -> Float:
    printf("A temperatura atual é %.2f <br>", 12.58); // %.2f é a limitação de casas decimais após a vírgula